#import "RedirectModule.h"

@implementation RedirectModule

+ (instancetype)modelWithName:(NSString *)name Password:(NSString *)pswd URL:(NSString *)url {
    RedirectModule *model = [RedirectModule new];
    model.username = name;
    model.password = pswd;
    model.url = url;
    
    return model;
}

@end
